package core_java.ENcapsulation;

public class EmployeeDetails {
    private int empid;
    private int empexp;
    String empName;

    public int getEmpid() {
        return empid;
    }

    public void setEmpid(int empid) {
        this.empid = empid;
    }

    public int getEmpexp() {
        return empexp;
    }

    public void setEmpexp(int empexp) {
        this.empexp = empexp;
    }

    public String getEmpname() {
        return empName;
    }

    public void setEmpname(String empname) {
        empName = empname;
    }

}
